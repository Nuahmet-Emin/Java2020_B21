package day03_Variables;

public class Escape_Sequences {

    public static void main(String[] args) {
            // \n: new line
        System.out.println("Hello Everyone \nMy name is Nurahmet Azat, I am from East Turkistan");

        // \t: tab
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("===============================");

        // \\: \ back slash
        System.out.println("/"); //  / print single front slash
        System.out.println("\\"); // \ print single back slash

        System.out.println("==============================");

        // \' single quote 1
        // ' single quote 2
        System.out.println("My name is 'Nurahmet'");
        System.out.println("My name is \'Nurahmet\'");

        // \": double quote
        System.out.println("My favorite TV series is\"Breaking Bad\" ");






    }
}
