package day23_NestedLoop;

public class DoWhile {
    public static void main(String[] args) {

        boolean condition = false;

        while(condition){ //think first about the condition
            System.out.println("Hello Batch 21");
        }

        System.out.println("===================================");

        do{
            System.out.println("Hello batch 21"); // do first, think later
        }while(condition);




    }
}
