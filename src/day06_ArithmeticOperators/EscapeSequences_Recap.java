package day06_ArithmeticOperators;

public class EscapeSequences_Recap {
    public static void main(String[] args) {

        /* \n: append new line
           \t: tab space, paragraph space
           \\: \
           \": double quote
         */
        System.out.println("\tHello Everyone\nHello Batch 21 \nToday I have learned a 'programming' language \"Java\"");

        System.out.println("===========================================");

        System.out.println("My favorite quote is:\n\t\"Winter is Coming\"\n\t\t\t\t- Ned Stark");

        System.out.println("===========================================");

        System.out.println("\\");


    }
}
