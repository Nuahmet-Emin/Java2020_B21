package day04_Variables;

public class CalculateCircle {

    public static void main(String[] args) {

        double r = 7.5;
        double pi = 3.14;

        double area = r * r * pi;
        double perimeter = 2 * pi * r;

        System.out.println("Area is: " + area);
        System.out.println("Perimeter is: " + perimeter);


    }
}
